#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	int			i;
	char		*s;
	char		*d;

	s = (char *)src;
	d = (char *)dst;
	if (src <= dst)
	{
		i = len - 1;
		while (i >= 0)
		{
			d[i] = s[i];
			i--;
		}
	}
	else
		ft_memcpy(d, s, len);
	return (dst);
}
