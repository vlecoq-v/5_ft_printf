#include "../ft_printf.h"

int		ft_check_type(char c)
{
	return ((c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i'
	|| c == 'o' || c == 'u' || c == 'x' || c == 'X' || c == 'f' || c == '%'
	|| c == 'b' || c == 'D' || c == 'U' || c == 'O' || c== 'F') ?
	1 : 0);
}
