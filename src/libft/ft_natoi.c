#include "libft.h"

int		ft_natoi(char *s, int n)
{
	int			si;
	long		nb;

	nb = 0;
	while (*s <= ' ')
		s++;
	si = (*s == '-') ? -1 : 1;
	(*s == '-' || *s == '+') ? s++ : 0;
	while ((*s >= '0' && *s <= '9') && n--)
		nb = nb * 10 + *s++ - '0';
	return ((int)nb * si);
}
