#include "libft.h"

void	ft_strrev(char *s)
{
	int		i;
	int		j;
	char	k;

	if (s)
	{
		i = 0;
		j = ft_strlen(s) - 1;
		while (i < j)
		{
			k = s[i];
			s[i] = s[j];
			s[j] = k;
			i++;
			j--;
		}
	}
}
